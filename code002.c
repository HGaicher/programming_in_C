#include <stdio.h>

int main()
{
	printf("Hello world!\n");		// neue Zeile
	printf("Hello\n world!\n");		// 2x neue Zeile
	printf("Hello\t world!\n");		// Tabulator
	printf("Hello\b world!\n");		// Backspace
	printf("Hello\r world!\n");		// Carriage return
	printf("Hello\a world!\n");		// Beep
	printf("Hello\007 world!\n");	// Beep
}
